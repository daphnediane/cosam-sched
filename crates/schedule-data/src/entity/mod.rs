/// Trait for all internal entity data structs generated by the macro.
/// Guarantees that every entity data struct carries its own UUID.
pub trait InternalData: Clone + Send + Sync + fmt::Debug {
    fn uuid(&self) -> NonNilUuid;
    fn set_uuid(&mut self, id: NonNilUuid);
}

/// Core trait for all entity types
pub trait EntityType: 'static + Send + Sync + fmt::Debug {
    type Data: InternalData;
    // @todo: type PublicData: PublicData;

    const TYPE_NAME: &'static str;
    const KIND: EntityKind;

    fn field_set() -> &'static FieldSet<Self>
    where
        Self: Sized;
    fn validate(data: &Self::Data) -> Result<(), ValidationError>
    where
        Self: Sized;

    /// Get the type name for this entity type (for dyn compatibility)
    fn type_name(&self) -> &'static str {
        Self::TYPE_NAME
    }

    /// Get the EntityKind for this entity type
    fn kind() -> EntityKind
    where
        Self: Sized,
    {
        Self::KIND
    }
}

/// Marker trait for entities that can be scheduled (assigned to panel types and event rooms)
pub trait SchedulableEntity: EntityType {}

/// Entity state for soft delete and status tracking
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum EntityState {
    #[default]
    Active,
    Inactive,
}

/// Identifies which entity type a UUID belongs to, used by the schedule's UUID registry.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum EntityKind {
    Panel,
    Presenter,
    EventRoom,
    HotelRoom,
    PanelType,
    PanelToPresenter,
    PanelToEventRoom,
    EventRoomToHotelRoom,
    PanelToPanelType,
    PresenterToGroup,
}

/// Trait for typed entity ID wrappers.
/// Provides a uniform interface for all entity IDs backed by `NonNilUuid`.
pub trait TypedId: Copy + Clone + Send + Sync + fmt::Debug + 'static {
    type EntityType: EntityType;

    fn non_nil_uuid(&self) -> NonNilUuid;

    fn uuid(&self) -> Uuid {
        self.non_nil_uuid().into()
    }

    fn kind() -> EntityKind {
        Self::EntityType::KIND
    }

    fn from_uuid(uuid: NonNilUuid) -> Self;

    fn try_from_raw_uuid(uuid: Uuid) -> Option<Self> {
        NonNilUuid::new(uuid).map(Self::from_uuid)
    }
}

/// A `NonNilUuid` tagged with the entity kind it refers to.
/// Returned by `Schedule::identify()`.
/// Variants are added as entity types are implemented.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum EntityUUID {
    EventRoom(EventRoomId),
    HotelRoom(HotelRoomId),
    Panel(PanelId),
    PanelType(PanelTypeId),
    Presenter(PresenterId),
    PresenterToGroup(PresenterToGroupId),
}

impl EntityUUID {
    pub fn non_nil_uuid(&self) -> NonNilUuid {
        match self {
            EntityUUID::EventRoom(id) => id.non_nil_uuid(),
            EntityUUID::HotelRoom(id) => id.non_nil_uuid(),
            EntityUUID::Panel(id) => id.non_nil_uuid(),
            EntityUUID::PanelType(id) => id.non_nil_uuid(),
            EntityUUID::Presenter(id) => id.non_nil_uuid(),
            EntityUUID::PresenterToGroup(id) => id.non_nil_uuid(),
        }
    }

    pub fn uuid(&self) -> Uuid {
        self.non_nil_uuid().into()
    }

    pub fn kind(&self) -> EntityKind {
        match self {
            EntityUUID::EventRoom(_) => EntityKind::EventRoom,
            EntityUUID::HotelRoom(_) => EntityKind::HotelRoom,
            EntityUUID::Panel(_) => EntityKind::Panel,
            EntityUUID::PanelType(_) => EntityKind::PanelType,
            EntityUUID::Presenter(_) => EntityKind::Presenter,
            EntityUUID::PresenterToGroup(_) => EntityKind::PresenterToGroup,
        }
    }
}

