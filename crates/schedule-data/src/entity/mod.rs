/// Trait for all internal entity data structs generated by the macro.
/// Guarantees that every entity data struct carries its own typed ID.
pub trait InternalData: Clone + Send + Sync + fmt::Debug {
    type Id: TypedId;

    /// Get the typed ID for this entity data.
    fn id(&self) -> Self::Id;

    /// Set the typed ID for this entity data.
    fn set_id(&mut self, id: Self::Id);
}

/// Core trait for all entity types
pub trait EntityType: 'static + Send + Sync + fmt::Debug {
    type Data: InternalData;
    type Id: TypedId<EntityType = Self>;

    const TYPE_NAME: &'static str;
    const KIND: EntityKind;

    fn field_set() -> &'static FieldSet<Self>
    where
        Self: Sized;
    fn validate(data: &Self::Data) -> Result<(), ValidationError>
    where
        Self: Sized;

    /// Get the type name for this entity type (for dyn compatibility)
    fn type_name(&self) -> &'static str {
        Self::TYPE_NAME
    }

    /// Get the EntityKind for this entity type
    fn kind() -> EntityKind
    where
        Self: Sized,
    {
        Self::KIND
    }

    /// Called after an entity is inserted via [`crate::schedule::EntityStorage::add_entity`]
    /// or [`crate::schedule::EntityStorage::add_with_uuid`].  Default is a no-op;
    /// override to populate reverse lookup indexes.
    fn on_insert(_storage: &mut crate::schedule::EntityStorage, _data: &Self::Data)
    where
        Self: Sized,
    {
    }

    /// Called during soft delete to remove edges pointing to this entity.
    ///
    /// This method is automatically called by `on_soft_delete` to clean up
    /// all edges that point to the entity being soft-deleted. This prevents
    /// dangling references in the EdgeMaps.
    ///
    /// The proc-macro generates implementations for each entity type that
    /// remove edges from all relevant EdgeMaps based on the entity's relationships.
    /// For example, a Panel will remove itself from `panels_by_panel_type`,
    /// `panels_by_event_room`, and `panels_by_presenter` EdgeMaps.
    ///
    /// Default is a no-op; the proc-macro provides entity-specific implementations.
    fn on_soft_delete_cleanup_edges(
        _storage: &mut crate::schedule::EntityStorage,
        _data: &Self::Data,
    ) where
        Self: Sized,
    {
    }

    /// Called by [`crate::schedule::EntityStorage::remove`] before the entity
    /// is removed.  The entity is still present in storage when this runs.
    /// Default is a no-op; override to clean up reverse lookup indexes.
    fn on_soft_delete(_storage: &mut crate::schedule::EntityStorage, _data: &Self::Data)
    where
        Self: Sized,
    {
        Self::on_soft_delete_cleanup_edges(_storage, _data);
    }

    /// Called by write closures after in-place mutation of entity data.
    /// `old` is the state before mutation, `new` is the current state.
    /// Default is a no-op; override to update reverse lookup indexes.
    fn on_update(
        _storage: &mut crate::schedule::EntityStorage,
        _old: &Self::Data,
        _new: &Self::Data,
    ) where
        Self: Sized,
    {
    }

    /// Resolve a FieldValue to an entity ID.
    ///
    /// Default implementation handles:
    /// - `FieldValue::NonNilUuid` -> direct UUID lookup
    /// - `FieldValue::String` -> delegates to `resolve_string`
    ///
    /// Override `resolve_string` for entity-specific resolution logic (e.g., PresenterEntityType::lookup_tagged).
    fn resolve_field_value(
        storage: &mut crate::schedule::EntityStorage,
        value: crate::field::FieldValue,
    ) -> Result<Self::Id, crate::field::FieldError>
    where
        Self: Sized + crate::schedule::TypedStorage,
    {
        match value {
            crate::field::FieldValue::NonNilUuid(uuid) => {
                if Self::contains_uuid(storage, uuid) {
                    Ok(Self::Id::from_uuid(uuid))
                } else {
                    Err(crate::field::FieldError::ConversionError(
                        crate::field::validation::ConversionError::InvalidFormat,
                    ))
                }
            }
            crate::field::FieldValue::String(s) => Self::resolve_string(storage, &s),
            _ => Err(crate::field::FieldError::ConversionError(
                crate::field::validation::ConversionError::UnsupportedType,
            )),
        }
    }

    /// Resolve a string to an entity ID.
    ///
    /// Default implementation handles:
    /// - UUID string parsing (with/without prefix) via `resolve_uuid_string`
    /// - match_index lookup for Uniq IDs
    ///
    /// Override for entity-specific string resolution logic (e.g., PresenterEntityType::lookup_tagged).
    fn resolve_string(
        storage: &mut crate::schedule::EntityStorage,
        input: &str,
    ) -> Result<Self::Id, crate::field::FieldError>
    where
        Self: Sized + crate::schedule::TypedStorage,
    {
        // Try UUID string parsing first
        if let Some(id) = Self::resolve_uuid_string(storage, input) {
            return Ok(id);
        }
        // Try match_index lookup
        if let Some(data) = storage.lookup_by_indexable::<Self>(input) {
            Ok(Self::Id::from_uuid(data.id().non_nil_uuid()))
        } else {
            Err(crate::field::FieldError::ConversionError(
                crate::field::validation::ConversionError::InvalidFormat,
            ))
        }
    }

    /// Resolve a UUID string to an entity ID.
    ///
    /// Handles bare UUID strings and prefixed UUID strings (e.g., "presenter-`<uuid>`").
    /// This is a helper method for entity types that need custom string resolution logic.
    fn resolve_uuid_string(
        storage: &crate::schedule::EntityStorage,
        input: &str,
    ) -> Option<Self::Id>
    where
        Self: Sized + crate::schedule::TypedStorage,
    {
        let uuid_str = if let Some(rest) =
            input.strip_prefix(&format!("{}-", Self::TYPE_NAME.to_lowercase()))
        {
            Some(rest)
        } else if Self::looks_like_uuid(input) {
            Some(input)
        } else {
            None
        };

        if let Some(uuid_str) = uuid_str {
            if let Ok(raw) = uuid_str.parse::<uuid::Uuid>() {
                if let Some(nn) = uuid::NonNilUuid::new(raw) {
                    if Self::contains_uuid(storage, nn) {
                        return Some(Self::Id::from_uuid(nn));
                    }
                }
            }
        }
        None
    }

    /// Check if the entity storage contains the given UUID.
    /// Default implementation uses the typed storage lookup.
    fn contains_uuid(storage: &crate::schedule::EntityStorage, uuid: uuid::NonNilUuid) -> bool
    where
        Self: Sized + crate::schedule::TypedStorage,
    {
        storage.get_by_uuid::<Self>(uuid).is_some()
    }

    /// Check if a string looks like a raw UUID (8-4-4-4-12 hex groups).
    fn looks_like_uuid(s: &str) -> bool {
        s.len() == 36
            && s.as_bytes().get(8) == Some(&b'-')
            && s.as_bytes().get(13) == Some(&b'-')
            && s.as_bytes().get(18) == Some(&b'-')
            && s.as_bytes().get(23) == Some(&b'-')
    }
}

